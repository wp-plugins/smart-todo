			<p>
				<h3>Thank you and Enjoy!</h3>
				This plugin creates Smart ToDo post types, todos are broken into tasks and categorized as Projects. Timers are added on each tasks.<br/>
				Created Projects as taxonomy for Smart ToDo post types. And each Smart ToDo post types is capable of adding multiple tasks with url references.<br/>

				Timers are provided on each task and can be controlled with buttons to Start, Pause and setting task as Done.<br/> Also edit buttons are provided to edit task description, task url reference and time spent. Proper user's access are also given to each type of users.<br/>

				SmartToDos manage page has columns for Projects and Status of the ToDo if it's Open or already Closed.<br/>	Total hours are provided dynamically and Title of window is set to active task with the current time spent. This is inspired from the Task Timer Google Chrome App.<br/>
			</p>
			<p>Plugin URI: <a href="http://www.sherkspear.com/portfolio-item/smart-todo-wordpress-plugin/" target="_blank">http://www.sherkspear.com/portfolio-item/smart-todo-wordpress-plugin/</a></p>
			
			<h3>Installation</h3>
			<ol>
				<li>BACKUP everything before you install the plugin.</li>
				<li>Upload smart-todo directory to the `/wp-content/plugins/` directory</li>
				<li>Activate the plugin through the 'Plugins' menu in WordPress</li>	
				<li>Update the Permalinks on settings.</li>
				<li>SmartToDo post type should now available at dashboard and start creating smart_todo post type.</li>
			</ol>
			
			<h3>Basic Instruction on Creating a Smart ToDo Post types</h3>

			<ol>
				<li>Create Smart ToDo Post Type</li>
				<li>Create Project category under Projects taxonomy for Smart ToDo post type.</li>
				<li>Add title and description on the ToDo and save.</li>
				<li>To add tasks on each ToDo, visit the ToDo at View ToDo(frontend) and start adding tasks with or without url references.</li>
				<li>When task is started to work on you can click start timer, pause and set the task to done.</li>
				<li>You can edit the task's description, edit url reference and also the time spent.</li>
			</ol>
			
		
